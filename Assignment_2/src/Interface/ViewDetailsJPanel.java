    // shows the information of the selected car
    private void populateCarDetails(){
        txtManufacturer.setText(car.getManufacturer());
        txtCity.setText(car.getCity());
        txtModel.setText(car.getModel());
        txtYear.setText(Integer.toString(car.getYear()));
        txtSeatNum.setText(Integer.toString(car.getSeatNum()));
        txtSerialNum.setText(Integer.toString(car.getSerialNumber()));
        
        boxExpired.setEnabled(false);
        boxAvailable.setEnabled(false);
        if (car.getExpired() == "Yes") {
            boxExpired.setSelected(true);
        }
        if (car.getAvailability() == "Available") {
            boxAvailable.setSelected(true);
        }       
        
        Date date = car.getCreatedOn();
        DateFormat dateFormat = new SimpleDateFormat("MM/dd/yyyy hh:mm:ss");
        String strDate = dateFormat.format(date);
        txtUpdatedTime.setText(strDate);
    }
    
