    // save edited car
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:  
       if (txtManufacturer.getText().isEmpty() == true) { 
           JOptionPane.showMessageDialog(null, "Please enter the Manufacturer");
       } else if (txtCity.getText().isEmpty() == true) {
           JOptionPane.showMessageDialog(null, "Please enter the City");
       } else if (txtModel.getText().isEmpty()) {
           JOptionPane.showMessageDialog(null, "Please enter the Model");
       } else if (txtYear.getText().isEmpty()) {
           JOptionPane.showMessageDialog(null, "Please enter the Year");
       } else if (txtSeatNum.getText().isEmpty()) {
           JOptionPane.showMessageDialog(null, "Please enter the Seat Number");
       } else if (txtSerialNum.getText().isEmpty()) {
           JOptionPane.showMessageDialog(null, "Please enter the Serial Number");
       } else {
            car.setManufacturer(txtManufacturer.getText());
            car.setCity(txtCity.getText());
            car.setModel(txtModel.getText());
            try {
                car.setYear(Integer.parseInt(txtYear.getText()));
                car.setSeatNum(Integer.parseInt(txtSeatNum.getText()));
                car.setSerialNumber(Integer.parseInt(txtSerialNum.getText()));
                String expired = "No";
                if (boxExpired.isSelected()) {
                    expired = "Yes";               
                }
                String availability = "Non-available";       
                if (boxAvailable.isSelected()) {
                    availability = "Available";               
                }
                car.setExpired(expired);
                car.setAvailability(availability);

                btnSave.setEnabled(false);
                btnUpdate.setEnabled(true);
                boxExpired.setEnabled(false);
                boxAvailable.setEnabled(false);

                JOptionPane.showMessageDialog(null, "Car Information Successfully Updated");
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Please enter integers for Year, Seat Number, and Serial Number");
            }
       }
    }//GEN-LAST:event_btnSaveActionPerformed


