    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) tblOrder.getModel();
        model.setRowCount(0);
        for (WorkRequest order : userAccount.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[5];
            row[0] = order;
            row[1] = order.getDish();
            row[2] = order.getSender().getEmployee().getName();             
            row[3] = order.getStatus();
            row[4] = order.getMessage();
            
            model.addRow(row);
        }
    }
    
    private void populateComboRest() {
        comboRest.removeAllItems();
        
        for (Organization org : system.getOrganizationList()) {
            if (org.getName().equals("RestaurantAdmin")) {
                
                for (UserAccount ua : org.getUserAccountDirectory().getUserAccountList()) {
                    //String rest = ua.getEmployee().getName();
                    comboRest.addItem(ua);
                }
                
            }
        }
    }
    
    private void populateComboDish(UserAccount ua) {
        comboDish.removeAllItems();
        
            for (String dish : ua.getMenu()) {
                comboDish.addItem(dish);
            }         
    }

    
