    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        // TODO add your handling code here:
        //WorkRequest request =  userAccount.getWorkQueue().getWorkRequestList().add(e)
        OrderWorkRequest order = new OrderWorkRequest();
        order.setReceiver(userAccount);
        UserAccount rest = (UserAccount)comboRest.getSelectedItem();
        order.setSender(rest);
        order.setStatus("order placed");
        order.setMessage("no comment yet");
        order.setDish(String.valueOf(comboDish.getSelectedItem()));
        
        userAccount.getWorkQueue().getWorkRequestList().add(order);
        
        populateRequestTable();
    }//GEN-LAST:event_btnPlaceOrderActionPerformed

