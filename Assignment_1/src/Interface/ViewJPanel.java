    // This method will pop the profile table
    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel)tblInfo.getModel();
        dtm.setRowCount(0);
        for (Info i: ih.getInfoHistory()) {
            Object row[] = new Object[2];
            row[0] = i;
            row[1] = i.getBirthday();
            dtm.addRow(row);
        }
    }

