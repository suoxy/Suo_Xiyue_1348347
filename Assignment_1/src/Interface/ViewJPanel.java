    // This method will show the details of stored profile when view details
    // button clicked
    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // find the selected profile and retrieve from history
        int selectedRow = tblInfo.getSelectedRow();
        if(selectedRow >= 0) {
            Info i = (Info)tblInfo.getValueAt(selectedRow, 0);
            image.setIcon(i.getPhoto());
            txtName.setText(i.getName());
            txtGeoData.setText(i.getGeographicData());
            txtBirthday.setText(i.getBirthday());
            txtTelephone.setText(String.valueOf(i.getTelephone()));
            txtFax.setText(String.valueOf(i.getFax()));
            txtEmail.setText(String.valueOf(i.getEmail()));
            txtSSN.setText(String.valueOf(i.getSsn()));
            txtMedicalNum.setText(String.valueOf(i.getMedicalRecordNum()));
            txtHealth.setText(String.valueOf(i.getHealthPlanNum()));
            txtBank.setText(String.valueOf(i.getBankAccountNum()));
            txtLicense.setText(i.getLicense());
            txtVehicle.setText(i.getVehicle());
            txtDevice.setText(i.getDevice());
            txtLinkedIn.setText(i.getLinkedIn());
            txtProtocol.setText(i.getProtocolAddress());
            txtBiometric.setText(i.getBioIndentifier());
            txtCode.setText(i.getPersonalCode());
        } else {
            // pop the notice when no row was selected
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed

    // This method will deleted a profile
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int selectedRow = tblInfo.getSelectedRow();
        if(selectedRow >= 0) {
            Info i = (Info)tblInfo.getValueAt(selectedRow, 0);
            ih.deleteInfo(i);
            JOptionPane.showMessageDialog(null, "Information has been deleted");
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
        
        // set the text field empty when the profile deleted
        image.setIcon(null);
        txtName.setText("");
        txtGeoData.setText("");
        txtBirthday.setText("");
        txtTelephone.setText("");
        txtFax.setText("");
        txtEmail.setText("");
        txtSSN.setText("");
        txtMedicalNum.setText("");
        txtHealth.setText("");
        txtBank.setText("");
        txtLicense.setText("");
        txtVehicle.setText("");
        txtDevice.setText("");
        txtLinkedIn.setText("");
        txtProtocol.setText("");
        txtBiometric.setText("");
        txtCode.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed


