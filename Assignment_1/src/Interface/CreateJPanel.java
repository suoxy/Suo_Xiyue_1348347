    // This method will save the profile attributes that user entered. 
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // read the infomation entered
        ImageIcon photo = (ImageIcon) image.getIcon();      // photo
        String name = txtName.getText();
        String geoData = txtGeoData.getText();
        String birthday = txtBirthday.getText();
        int telephone = Integer.parseInt(txtTelephone.getText());
        int fax = Integer.parseInt(txtFax.getText());
        String email = txtEmail.getText();
        int ssn = Integer.parseInt(txtSSN.getText());
        int medicalRecordNum = Integer.parseInt(txtMedicalNum.getText());
        int healthPlanNum = Integer.parseInt(txtHealth.getText());
        int bankAccountNum = Integer.parseInt(txtBank.getText());
        String license = txtLicense.getText();
        String vehicle = txtVehicle.getText();
        String device = txtDevice.getText();
        String linkedIn = txtLinkedIn.getText();
        String protocolAddress = txtProtocol.getText();
        String bioIndentifier = txtBiometric.getText();
        String personalCode = txtCode.getText();
        // add the new infomation to the history
        Info i = ih.addInfo();
        i.setPhoto(photo);
        i.setName(name);
        i.setGeographicData(geoData);
        i.setBirthday(birthday);
        i.setTelephone(telephone);
        i.setFax(fax);
        i.setEmail(email);
        i.setSsn(ssn);
        i.setMedicalRecordNum(medicalRecordNum);
        i.setHealthPlanNum(healthPlanNum);
        i.setBankAccountNum(bankAccountNum);
        i.setLicense(license);
        i.setVehicle(vehicle);
        i.setDevice(device);
        i.setLinkedIn(linkedIn);
        i.setProtocolAddress(protocolAddress);
        i.setBioIndentifier(bioIndentifier);
        i.setPersonalCode(personalCode);
        
        JOptionPane.showMessageDialog(null, "Information Added Successful");
        
        emptyText();    
    }//GEN-LAST:event_btnSaveActionPerformed
    
